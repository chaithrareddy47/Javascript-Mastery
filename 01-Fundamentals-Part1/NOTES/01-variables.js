// ==========================================
// VARIABLES
// ==========================================

// A value is a piece of data.
// Examples:
// "Jonas" -> String
// 23 -> Number
// true -> Boolean

console.log("Jonas");

// Declaring a variable
let firstName = "Namaste";

console.log(firstName);

// console.log() is used to print output to the console.

/*
===========================
Variable Naming Rules
===========================

1. Use camelCase
   Example:
   firstName
   studentAge
   totalMarks

2. Variable names cannot start with a number.
   ❌ let 1name = "Akshay";
   ✅ let name1 = "Akshay";

3. Variable names can contain:
   - Letters (a-z, A-Z)
   - Numbers (not at the beginning)
   - Underscore (_)
   - Dollar sign ($)

4. Variable names cannot contain spaces.
   ❌ first name
   ✅ firstName

5. Do not use JavaScript reserved keywords.
   Example:
   let, const, function, if, return

6. Variable names are case-sensitive.
   age !== Age

7. Variable names should be meaningful.
   ❌ let x = 22;
   ✅ let studentAge = 22;

8. By convention:
   Variables -> camelCase
   Constants -> UPPER_CASE (optional)
   Classes -> PascalCase
*/

/*
Best Practices

✔ Use meaningful variable names.

✔ Use const by default.

✔ Use let only when the value changes.

✔ Avoid using var in modern JavaScript.
*/
