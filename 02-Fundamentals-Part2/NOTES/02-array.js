/*
ARRAYS
What is an Array?
An array is a special data structure used to store multiple values in a single variable.

Instead of creating many variables, we can store all related values together

*/

//without array
const student1 = "john";
const student2 = "alice";
const student3 = "bob";

// with array
const students = ["john", "alice", "bob"]
console.log(students);

// array starts from 0

//accessing elements
const fruits = ["apple", "banana", "mango"]
console.log(fruits[0]);
console.log(fruits[1]);

//updating element
fruits[1] = "orange";
console.log(fruits);

//array length
console.log(fruits.length);





